const compiledLanguage = typeof HV6_DASHBOARD_LANG !== 'undefined' ? String(HV6_DASHBOARD_LANG).toLowerCase() : 'en';
const currentLanguage = dictionaries[compiledLanguage] ? compiledLanguage : 'en';

export function subscribeLanguage(fn) {
  // Language is fixed at build time with LANG=<en|da>.
  // Components can keep calling this from shared i18n-aware code; there is no
  // runtime language event to subscribe to.
  void fn;
}

export function t(key, vars) {
  const value = (dictionaries[currentLanguage] && dictionaries[currentLanguage][key]) ||
    dictionaries.en[key] ||
    key;
  if (!vars) return value;
  return String(value).replace(/\{(\w+)\}/g, (_, name) => vars[name] == null ? '' : String(vars[name]));
}

export function localize(root) {
  if (!root) return;
  root.querySelectorAll('[data-i18n]').forEach((node) => {
    node.textContent = t(node.getAttribute('data-i18n'));
  });
  root.querySelectorAll('[data-i18n-title]').forEach((node) => {
    node.setAttribute('title', t(node.getAttribute('data-i18n-title')));
  });
  root.querySelectorAll('[data-i18n-label]').forEach((node) => {
    node.setAttribute('aria-label', t(node.getAttribute('data-i18n-label')));
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    node.setAttribute('placeholder', t(node.getAttribute('data-i18n-placeholder')));
  });
}

if (typeof document !== 'undefined') document.documentElement.setAttribute('lang', currentLanguage);
